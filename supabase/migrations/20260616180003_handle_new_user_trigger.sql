-- Mirror every new auth.users row into public.profiles so application code can
-- key everything off the profile row without a separate join into the auth
-- schema. The trigger runs with definer rights because auth.users is owned by
-- supabase_auth_admin.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (user_id, display_name)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'display_name', split_part(new.email, '@', 1))
  );
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();
